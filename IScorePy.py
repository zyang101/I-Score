def save_video_to_input_folder(frame_dir, input_video_path, fps, output_name="combined_output.mp4"):
    """
    Takes all frames in frame_dir and writes a video in the same folder as input_video_path.
    """
    # Determine parent directory
    parent_dir = os.path.dirname(input_video_path)
    output_path = os.path.join(parent_dir, output_name)

    # Collect frames and sort naturally
    frames = [f for f in os.listdir(frame_dir) if f.lower().endswith((".jpg", ".png", ".jpeg"))]
    frames = natsorted(frames)

    if not frames:
        print("❗ No frames found in", frame_dir)
        return None

    # Read first frame to get resolution
    first = cv2.imread(os.path.join(frame_dir, frames[0]))
    h, w, _ = first.shape

    # Create VideoWriter
    fourcc = cv2.VideoWriter_fourcc(*'avc1')  # H.264 - better compatibility
    out = cv2.VideoWriter(output_path, fourcc, fps, (w, h))

    # Write all frames
    for f in frames:
        img = cv2.imread(os.path.join(frame_dir, f))
        out.write(img)

    out.release()
    print("✅ Video saved to:", output_path)
    return output_path

